def test_with_correct_dates(correct_transactions):
    """Тест с корректным форматом дат"""
    result = spending_by_category(correct_transactions, "Еда", "01.04.2023")
    assert len(result) == 3
    assert result["Сумма операции"].sum() == -1200


def test_edge_cases():
    """Тест граничных случаев с точным учётом логики фильтрации"""
    # Используем end_date на день позже нужного нам периода
    reporting_end_date = "02.04.2023"

    test_data = [
        # Должна быть включена (ровно 3 месяца назад от reporting_end_date)
        {"Дата операции": "02.01.2023 00:00:00", "Категория": "Еда", "Сумма операции": -100},
        # Должна быть включена (последний момент 1 апреля)
        {"Дата операции": "01.04.2023 23:59:59", "Категория": "Еда", "Сумма операции": -200},
        # Не должна быть включена (слишком рано)
        {"Дата операции": "01.01.2023 23:59:59", "Категория": "Еда", "Сумма операции": -300},
    ]

    df = pd.DataFrame(test_data)
    result = spending_by_category(df, "Еда", reporting_end_date)

    # Должно быть 2 транзакции
    assert len(result) == 2

    # Проверяем суммы
    assert result["Сумма операции"].sum() == -300

    # Проверяем что нет слишком ранней транзакции
    assert "01.01.2023" not in result["Дата операции"].dt.strftime("%d.%m.%Y").values


def test_invalid_dates():
    """Тест с некорректными датами (должен упасть, так как reports.py не обрабатывает ошибки)"""
    invalid_data = pd.DataFrame(
        [
            {"Дата операции": "2023-01-01 12:00", "Категория": "Еда", "Сумма операции": -100},  # Неправильный формат
            {
                "Дата операции": "01/01/2023 12:00:00",
                "Категория": "Еда",
                "Сумма операции": -200,
            },  # Неправильный разделитель
        ]
    )

    with pytest.raises(ValueError):
        spending_by_category(invalid_data, "Еда", "01.04.2023")
